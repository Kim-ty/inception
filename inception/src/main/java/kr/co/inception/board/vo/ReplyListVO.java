package kr.co.inception.board.vo;

public class ReplyVO {

	private String ridx;
	private String targetreply;
	private String contents;
	private String level;
	
	public String getRidx() {
		return ridx;
	}
	public void setRidx(String ridx) {
		this.ridx = ridx;
	}
	public String getTargetreply() {
		return targetreply;
	}
	public void setTargetreply(String targetreply) {
		this.targetreply = targetreply;
	}
	public String getContents() {
		return contents;
	}
	public void setContents(String contents) {
		this.contents = contents;
	}
	public String getLevel() {
		return level;
	}
	public void setLevel(String level) {
		this.level = level;
	}
	
	
	
}
